package lucas.g.educacion.it;

public class Usuario {
	
	private final String id;
	private final String nombre;
	
	public Usuario(String id, String nombre) {
		super();
		this.id = id;
		this.nombre = nombre;
	}

	public String getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}
	
	
	
	
	

}
