package ar.com.telecom.shiva.presentacion.bean.dto;

import ar.com.telecom.shiva.base.dto.DTO;


public class GestionDto extends DTO {

	private static final long serialVersionUID = 1L;
	
	private String idEmpresa;
	private String idSegmento;
	
	/* Flags */
	private boolean comboEmpresa=false;
	private boolean comboSegmento=false;
	
	//Error general
	private String errorGeneral;
	
	public GestionDto(){
	}

	
	/*****************************
	 * Setters & Getters         *
	 *****************************/

	public boolean isComboEmpresa() {
		return comboEmpresa;
	}

	public void setComboEmpresa(boolean comboEmpresa) {
		this.comboEmpresa = comboEmpresa;
	}

	public boolean isComboSegmento() {
		return comboSegmento;
	}

	public void setComboSegmento(boolean comboSegmento) {
		this.comboSegmento = comboSegmento;
	}

	public String getIdEmpresa() {
		return idEmpresa;
	}

	public void setIdEmpresa(String idEmpresa) {
		this.idEmpresa = idEmpresa;
	}


	public String getIdSegmento() {
		return idSegmento;
	}


	public void setIdSegmento(String idSegmento) {
		this.idSegmento = idSegmento;
	}
	
	public String getErrorGeneral() {
		return errorGeneral;
	}

	public void setErrorGeneral(String errorGeneral) {
		this.errorGeneral = errorGeneral;
	}
}
