package ar.com.telecom.shiva.base.ws.cliente.datos.salida.agrupador.sap;

public class ResultadoInvocacion {

	protected String resultadoInvocacion;
	protected String codigoError;
	protected String descripcionError;
    
	public ResultadoInvocacion() {}
    
	public String toString() {
		return "Resultado[" + this.resultadoInvocacion + "-" + this.codigoError + ":" + this.descripcionError + "]";
	}

	/**
	 * @return the resultadoInvocacion
	 */
	public String getResultadoInvocacion() {
		return resultadoInvocacion;
	}

	/**
	 * @param resultadoInvocacion the resultadoInvocacion to set
	 */
	public void setResultadoInvocacion(String resultadoInvocacion) {
		this.resultadoInvocacion = resultadoInvocacion;
	}

	/**
	 * @return the codigoError
	 */
	public String getCodigoError() {
		return codigoError;
	}

	/**
	 * @param codigoError the codigoError to set
	 */
	public void setCodigoError(String codigoError) {
		this.codigoError = codigoError;
	}

	/**
	 * @return the descripcionError
	 */
	public String getDescripcionError() {
		return descripcionError;
	}

	/**
	 * @param descripcionError the descripcionError to set
	 */
	public void setDescripcionError(String descripcionError) {
		this.descripcionError = descripcionError;
	}
}
