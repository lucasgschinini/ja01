package ar.com.telecom.shiva.presentacion.bean.filtro;

import java.util.ArrayList;
import java.util.List;
import ar.com.telecom.shiva.base.enumeradores.EmpresaEnum;
import ar.com.telecom.shiva.negocio.workflow.definicion.Estado;

/**
 * @author juan.f.villagra
 *
 */
public class VistaSoporteImputacionAutomaticaValoresClientesPurosFiltro extends Filtro {

	private static final long serialVersionUID = -2579427009550076872L;

	private EmpresaEnum empresaCliente;
	private List<Estado> listaEstados = new ArrayList<Estado>();
	
	public VistaSoporteImputacionAutomaticaValoresClientesPurosFiltro(EmpresaEnum empresaCliente, List<Estado> listaEstados) {
		this.empresaCliente = empresaCliente;
		this.listaEstados = listaEstados;
	}

	public EmpresaEnum getEmpresaCliente() {
		return empresaCliente;
	}

	public void setEmpresaCliente(EmpresaEnum empresaCliente) {
		this.empresaCliente = empresaCliente;
	}

	public List<Estado> getListaEstados() {
		return listaEstados;
	}

	public void setListaEstados(List<Estado> listaEstados) {
		this.listaEstados = listaEstados;
	}
	
	
}
