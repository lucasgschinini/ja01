package ar.com.telecom.shiva.base.ws.cliente.datos.entrada.agrupador.calipso;

import java.io.Serializable;
import java.math.BigInteger;
import java.util.Date;

public class InformacionParaPaginadoDebito implements Serializable {
		
	private static final long serialVersionUID = 1L;
	
	protected BigInteger 	cantidadDeRegistrosARetornar;
	protected BigInteger 	ultimoIdDocCtasCob;
	protected Date 			ultimaFechaDocumento;
	protected BigInteger 	ultimoClienteDocumento;
	
	public BigInteger getCantidadDeRegistrosARetornar() {
		return cantidadDeRegistrosARetornar;
	}
	public void setCantidadDeRegistrosARetornar(BigInteger cantidadDeRegistrosARetornar) {
		this.cantidadDeRegistrosARetornar = cantidadDeRegistrosARetornar;
	}
	public BigInteger getUltimoIdDocCtasCob() {
		return ultimoIdDocCtasCob;
	}
	public void setUltimoIdDocCtasCob(BigInteger ultimoIdDocCtasCob) {
		this.ultimoIdDocCtasCob = ultimoIdDocCtasCob;
	}
	public BigInteger getUltimoClienteDocumento() {
		return ultimoClienteDocumento;
	}
	public void setUltimoClienteDocumento(BigInteger ultimoClienteDocumento) {
		this.ultimoClienteDocumento = ultimoClienteDocumento;
	}
	public Date getUltimaFechaDocumento() {
		return ultimaFechaDocumento;
	}
	public void setUltimaFechaDocumento(Date ultimaFechaDocumento) {
		this.ultimaFechaDocumento = ultimaFechaDocumento;
	}
}
