package ar.com.telecom.shiva.presentacion.bean.dto.json;

import ar.com.telecom.shiva.base.enumeradores.TipoSimularDisabled;

public class HabilitacionBtnSimularJsonResponse extends JsonResponse {

	private Long idCobro;
	private EstadoCobroJsonResponse estado = new EstadoCobroJsonResponse();
	private boolean disabled = false;
	private TipoSimularDisabled causa = null;

	public HabilitacionBtnSimularJsonResponse() {
		
	}

	/**
	 * @return the idCobro
	 */
	public Long getIdCobro() {
		return idCobro;
	}

	/**
	 * @param idCobro the idCobro to set
	 */
	public void setIdCobro(Long idCobro) {
		this.idCobro = idCobro;
	}

	/**
	 * @return the estado
	 */
	public EstadoCobroJsonResponse getEstado() {
		return estado;
	}

	/**
	 * @param estado the estado to set
	 */
	public void setEstado(EstadoCobroJsonResponse estado) {
		this.estado = estado;
	}

	/**
	 * @return the disabled
	 */
	public boolean isDisabled() {
		return disabled;
	}

	/**
	 * @param disabled the disabled to set
	 */
	public void setDisabled(boolean disabled) {
		this.disabled = disabled;
	}

	/**
	 * @return the causa
	 */
	public TipoSimularDisabled getCausa() {
		return causa;
	}

	/**
	 * @param causa the causa to set
	 */
	public void setCausa(TipoSimularDisabled causa) {
		this.causa = causa;
	}
	
}
