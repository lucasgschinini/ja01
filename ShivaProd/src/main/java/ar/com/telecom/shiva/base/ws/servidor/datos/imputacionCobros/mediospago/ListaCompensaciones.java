package ar.com.telecom.shiva.base.ws.servidor.datos.imputacionCobros.mediospago;

import java.util.ArrayList;
import java.util.Collection;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "listaCompensaciones")
public class ListaCompensaciones {

	@XmlElement(name = "compensacion")
	private Collection<Compensacion> compensaciones = new ArrayList<Compensacion>();

	public Collection<Compensacion> getCompensaciones() {
		return compensaciones;
	}

	public void setCompensaciones(Collection<Compensacion> compensaciones) {
		this.compensaciones = compensaciones;
	}

	
}
