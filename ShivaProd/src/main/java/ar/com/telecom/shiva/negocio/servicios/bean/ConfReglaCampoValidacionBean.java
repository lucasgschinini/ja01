package ar.com.telecom.shiva.negocio.servicios.bean;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * @author U587496 Guido.Settecerze
 *
 */
public class ConfReglaCampoValidacionBean implements Serializable {
	private static final long serialVersionUID = 20180613L;

	private String tipoCampo;	
	private String nombre;
	private String tipoDeDato;
	private String validacion;
	private BigDecimal orden;
	private Long longitud;
	
	public ConfReglaCampoValidacionBean() {
	}

	/**
	 * @return the tipoCampo
	 */
	public String getTipoCampo() {
		return tipoCampo;
	}

	/**
	 * @param tipoCampo the tipoCampo to set
	 */
	public void setTipoCampo(String tipoCampo) {
		this.tipoCampo = tipoCampo;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}


	/**
	 * @return the tipoDeDato
	 */
	public String getTipoDeDato() {
		return tipoDeDato;
	}

	/**
	 * @param tipoDeDato the tipoDeDato to set
	 */
	public void setTipoDeDato(String tipoDeDato) {
		this.tipoDeDato = tipoDeDato;
	}

	/**
	 * @return the validacion
	 */
	public String getValidacion() {
		return validacion;
	}

	/**
	 * @param validacion the validacion to set
	 */
	public void setValidacion(String validacion) {
		this.validacion = validacion;
	}

	/**
	 * @return the orden
	 */
	public BigDecimal getOrden() {
		return orden;
	}

	/**
	 * @param orden the orden to set
	 */
	public void setOrden(BigDecimal orden) {
		this.orden = orden;
	}

	/**
	 * @return the longitud
	 */
	public Long getLongitud() {
		return longitud;
	}

	/**
	 * @param longitud the longitud to set
	 */
	public void setLongitud(Long longitud) {
		this.longitud = longitud;
	}

	

	
}
