package ar.com.telecom.shiva.base.ws.servidor.datos.imputacionCobros.mediospago;

import java.util.ArrayList;
import java.util.Collection;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "listaPlanesDePago")
public class ListaPlanesDePago {

	@XmlElement(name = "planDePago")
	private Collection<PlanDePago> planesDePago = new ArrayList<PlanDePago>();

	public Collection<PlanDePago> getPlanesDePago() {
		return planesDePago;
	}

	public void setPlanesDePago(Collection<PlanDePago> planesDePago) {
		this.planesDePago = planesDePago;
	}
	
}
