package ar.com.telecom.shiva.negocio.dto;

import java.io.Serializable;

/**
 * @author u578936 M.A.Uehara
 *
 */
public class ProvinciaParametroDto implements Serializable {
	private static final long serialVersionUID = 20170806L;

	private String idProvincia;
	private String descripcion;

	public ProvinciaParametroDto() {}

	/**
	 * @return the idProvincia
	 */
	public String getIdProvincia() {
		return idProvincia;
	}

	/**
	 * @param idProvincia the idProvincia to set
	 */
	public void setIdProvincia(String idProvincia) {
		this.idProvincia = idProvincia;
	}

	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}

	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
}
