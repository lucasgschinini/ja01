package ar.com.telecom.shiva.presentacion.bean.filtro;


public class VistaSoporteCobroTransaccionFiltro extends Filtro {

	private static final long serialVersionUID = 1L;
	
	private Long 	idCobro;
	private Long 	idOperacion;
	private String	sistema;
	private String	sociedad;

	/**
	 * @return the idCobro
	 */
	public Long getIdCobro() {
		return idCobro;
	}
	/**
	 * @param idCobro the idCobro to set
	 */
	public void setIdCobro(Long idCobro) {
		this.idCobro = idCobro;
	}
	/**
	 * @return the idOperacion
	 */
	public Long getIdOperacion() {
		return idOperacion;
	}
	/**
	 * @param idOperacion the idOperacion to set
	 */
	public void setIdOperacion(Long idOperacion) {
		this.idOperacion = idOperacion;
	}
	/**
	 * @return the sistema
	 */
	public String getSistema() {
		return sistema;
	}
	/**
	 * @param string the sistema to set
	 */
	public void setSistema(String string) {
		this.sistema = string;
	}
	/**
	 * @return the sociedad
	 */
	public String getSociedad() {
		return sociedad;
	}
	/**
	 * @param sociedad the sociedad to set
	 */
	public void setSociedad(String sociedad) {
		this.sociedad = sociedad;
	}
	
	
}
