package ar.com.telecom.shiva.base.ws.cliente.datos.salida.agrupador.deimos;

public class ResultadoApropiacionDocumento {

	protected String resultadoApropiacion;
	protected String codigoError;
	protected String descripcionError;
    
	public ResultadoApropiacionDocumento() {}
    
	public String toString() {
		return "ResultadoProcesamiento["+this.resultadoApropiacion+"-"+this.codigoError+":"+this.descripcionError+"]";
	}

	public String getCodigoError() {
		return codigoError;
	}

	public void setCodigoError(String codigoError) {
		this.codigoError = codigoError;
	}

	public String getDescripcionError() {
		return descripcionError;
	}

	public void setDescripcionError(String descripcionError) {
		this.descripcionError = descripcionError;
	}

	public String getResultadoApropiacion() {
		return resultadoApropiacion;
	}

	public void setResultadoApropiacion(String resultadoApropiacion) {
		this.resultadoApropiacion = resultadoApropiacion;
	}
}
