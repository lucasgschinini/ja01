package ar.com.telecom.shiva.persistencia.bean;

import ar.com.telecom.shiva.base.enumeradores.EstadoTransaccionEnum;
import ar.com.telecom.shiva.base.enumeradores.SistemaEnum;
import ar.com.telecom.shiva.base.enumeradores.SociedadEnum;

public class ResultadoBusquedaEstadoGrupos {

	Long grupo;
	EstadoTransaccionEnum estadoGrupo;
	EstadoTransaccionEnum estadoTransaccion;
	SociedadEnum sociedad;
	SistemaEnum sistema;
	
	
	public SociedadEnum getSociedad() {
		return sociedad;
	}
	public void setSociedad(SociedadEnum sociedad) {
		this.sociedad = sociedad;
	}
	public SistemaEnum getSistema() {
		return sistema;
	}
	public void setSistema(SistemaEnum sistema) {
		this.sistema = sistema;
	}
	public Long getGrupo() {
		return grupo;
	}
	public void setGrupo(Long grupo) {
		this.grupo = grupo;
	}
	public EstadoTransaccionEnum getEstadoGrupo() {
		return estadoGrupo;
	}
	public void setEstadoGrupo(EstadoTransaccionEnum estadoGrupo) {
		this.estadoGrupo = estadoGrupo;
	}
	public EstadoTransaccionEnum getEstadoTransaccion() {
		return estadoTransaccion;
	}
	public void setEstadoTransaccion(EstadoTransaccionEnum estadoTransaccion) {
		this.estadoTransaccion = estadoTransaccion;
	}
	
	
}
