package ar.com.telecom.shiva.base.ws.servidor.datos.RecepcionNovedadesDocumentosSAP;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "documentoOriginal" )
public class DocumentoSap  extends Object implements Serializable {

	private static final long serialVersionUID = 1L;
	
	@XmlElement(name = "idDocumento", required = true)
	private String idDocumento;
	
	@XmlElement(name = "idSociedad", required = true)
	private String idSociedad;
	
	@XmlElement(name = "anioFiscal", required = true)
	private Long anioFiscal;
	
	

	public String getIdDocumento() {
		return idDocumento;
	}

	public void setIdDocumento(String idDocumento) {
		this.idDocumento = idDocumento;
	}

	public String getIdSociedad() {
		return idSociedad;
	}

	public void setIdSociedad(String idSociedad) {
		this.idSociedad = idSociedad;
	}

	public Long getAnioFiscal() {
		return anioFiscal;
	}

	public void setAnioFiscal(Long anioFiscal) {
		this.anioFiscal = anioFiscal;
	}
	
	

}
