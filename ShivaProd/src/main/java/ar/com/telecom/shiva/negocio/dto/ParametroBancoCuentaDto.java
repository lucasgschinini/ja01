public class ParametroBancoCuentaDto extends DTO {
	private static final long serialVersionUID = 20170520L;

	private Integer idBancoCuenta;
	private ParametroBancoDto banco;
	private String cuentaBancaria;
	/**
	 * @return the banco
	 */
	public ParametroBancoDto getBanco() {
		return banco;
	}
	/**
	 * @param banco the banco to set
	 */
	public void setBanco(ParametroBancoDto banco) {
		this.banco = banco;
	}
	/**
	 * @return the cuentaBancaria
	 */
	public String getCuentaBancaria() {
		return cuentaBancaria;
	}
	/**
	 * @param cuentaBancaria the cuentaBancaria to set
	 */
	public void setCuentaBancaria(String cuentaBancaria) {
		this.cuentaBancaria = cuentaBancaria;
	}
	/**
	 * @return the idBancoCuenta
	 */
	public Integer getIdBancoCuenta() {
		return idBancoCuenta;
	}
	/**
	 * @param idBancoCuenta the idBancoCuenta to set
	 */
	public void setIdBancoCuenta(Integer idBancoCuenta) {
		this.idBancoCuenta = idBancoCuenta;
	}
	
	
}
