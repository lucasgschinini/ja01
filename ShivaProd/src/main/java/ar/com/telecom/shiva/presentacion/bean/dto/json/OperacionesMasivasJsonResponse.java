public class OperacionesMasivasJsonResponse extends JsonResponse {

	private List<OperacionMasivaDto> aaData;
	private String errorMensaje;
	private ArrayList<ErrorJson> errores;
	
	public List<OperacionMasivaDto> getAaData() {
		return aaData;
	}

	public void setAaData(List<OperacionMasivaDto> aaData) {
		this.aaData = aaData;
	}

	public String getErrorMensaje() {
		return errorMensaje;
	}

	public void setErrorMensaje(String errorMensaje) {
		this.errorMensaje = errorMensaje;
	}
	
	public ArrayList<ErrorJson> getErrores() {
		return errores;
	}

	public void setErrores(ArrayList<ErrorJson> errores) {
		this.errores = errores;
	}
	
}
