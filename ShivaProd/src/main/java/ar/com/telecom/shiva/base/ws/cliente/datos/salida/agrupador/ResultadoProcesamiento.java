package ar.com.telecom.shiva.base.ws.cliente.datos.salida.agrupador;

public class ResultadoProcesamiento {

	protected String resultadoImputacion;
	protected String codigoError;
	protected String descripcionError;
    
	public ResultadoProcesamiento() {}
    
	public String toString() {
		return "ResultadoProcesamiento["+this.resultadoImputacion+"-"+this.codigoError+":"+this.descripcionError+"]";
	}

	public String getResultadoImputacion() {
		return resultadoImputacion;
	}

	public void setResultadoImputacion(String resultadoImputacion) {
		this.resultadoImputacion = resultadoImputacion;
	}

	public String getCodigoError() {
		return codigoError;
	}

	public void setCodigoError(String codigoError) {
		this.codigoError = codigoError;
	}

	public String getDescripcionError() {
		return descripcionError;
	}

	public void setDescripcionError(String descripcionError) {
		this.descripcionError = descripcionError;
	}
	
}
