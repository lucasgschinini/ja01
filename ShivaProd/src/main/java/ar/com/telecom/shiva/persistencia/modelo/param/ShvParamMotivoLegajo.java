package ar.com.telecom.shiva.persistencia.modelo.param;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

import ar.com.telecom.shiva.persistencia.modelo.Modelo;

@Entity
@Table(name = "SHV_PARAM_MOTIVO_LEGAJO")
public class ShvParamMotivoLegajo extends Modelo{
	
	private static final long serialVersionUID = 1L;
	
	@Id
	@Column (name="ID_MOTIVO_LEGAJO")
	private Integer idMotivoLegajo;
	
	@Column (name="DESCRIPCION")
	private String descripcion;

	/**
	 * @return the idMotivoLegajo
	 */
	public Integer getIdMotivoLegajo() {
		return idMotivoLegajo;
	}

	/**
	 * @param idMotivoLegajo the idMotivoLegajo to set
	 */
	public void setIdMotivoLegajo(Integer idMotivoLegajo) {
		this.idMotivoLegajo = idMotivoLegajo;
	}

	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}

	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
