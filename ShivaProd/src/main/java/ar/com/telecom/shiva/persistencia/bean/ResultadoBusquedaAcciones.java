package ar.com.telecom.shiva.persistencia.bean;

public class ResultadoBusquedaAcciones {

	Long idAccion;
	String descripcionAccion;
	String idPerfilAplicativo;
	String idPerfil;
	String usuario;
	
	/**
	 * @return the idAccion
	 */
	public Long getIdAccion() {
		return idAccion;
	}
	/**
	 * @param idAccion the idAccion to set
	 */
	public void setIdAccion(Long idAccion) {
		this.idAccion = idAccion;
	}
	/**
	 * @return the descripcionAccion
	 */
	public String getDescripcionAccion() {
		return descripcionAccion;
	}
	/**
	 * @param descripcionAccion the descripcionAccion to set
	 */
	public void setDescripcionAccion(String descripcionAccion) {
		this.descripcionAccion = descripcionAccion;
	}
	/**
	 * @return the idPerfilAplicativo
	 */
	public String getIdPerfilAplicativo() {
		return idPerfilAplicativo;
	}
	/**
	 * @param idPerfilAplicativo the idPerfilAplicativo to set
	 */
	public void setIdPerfilAplicativo(String idPerfilAplicativo) {
		this.idPerfilAplicativo = idPerfilAplicativo;
	}
	/**
	 * @return the idPerfil
	 */
	public String getIdPerfil() {
		return idPerfil;
	}
	/**
	 * @param idPerfil the idPerfil to set
	 */
	public void setIdPerfil(String idPerfil) {
		this.idPerfil = idPerfil;
	}
	/**
	 * @return the usuario
	 */
	public String getUsuario() {
		return usuario;
	}
	/**
	 * @param usuario the usuario to set
	 */
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	
	
	
}
