package ar.com.telecom.shiva.base.ws.cliente.datos.salida.agrupador.calipso;

public class InformacionAdicionalDacota {

	protected String unidadOperativa;
	protected String subTipo;
	protected String holding;
	
	public String getUnidadOperativa() {
		return unidadOperativa;
	}
	public void setUnidadOperativa(String unidadOperativa) {
		this.unidadOperativa = unidadOperativa;
	}
	
	public String getSubTipo() {
		return subTipo;
	}
	public void setSubTipo(String subTipo) {
		this.subTipo = subTipo;
	}
	
	public String getHolding() {
		return holding;
	}
	public void setHolding(String holding) {
		this.holding = holding;
	}
}
