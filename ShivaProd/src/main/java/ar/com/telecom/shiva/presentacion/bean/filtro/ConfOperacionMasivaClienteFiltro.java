package ar.com.telecom.shiva.presentacion.bean.filtro;

public class ConfOperacionMasivaClienteFiltro extends Filtro {
	
	private static final long serialVersionUID = 1L;

	private String criterio;
	private String busqueda;

	public ConfOperacionMasivaClienteFiltro() {
		super();
		this.criterio = "";
		this.busqueda = "";
	}
	
	public ConfOperacionMasivaClienteFiltro clone() {
		ConfOperacionMasivaClienteFiltro clone = new ConfOperacionMasivaClienteFiltro();
		clone.setCriterio(this.criterio);
		clone.setBusqueda(this.busqueda);
		return clone;
	}

	public String getCriterio() {
		return criterio;
	}

	public void setCriterio(String criterio) {
		this.criterio = criterio;
	}

	public String getBusqueda() {
		return busqueda;
	}

	public void setBusqueda(String busqueda) {
		this.busqueda = busqueda;
	}
	
}
