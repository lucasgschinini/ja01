package ar.com.telecom.shiva.persistencia.modelo;



import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;


@Entity
@Table (name = "SHV_MIG_CONSTANCIA")
public class ShvMigConstancia extends Modelo{

	private static final long serialVersionUID = 1L;
	
	@Id
	@Column(name="ID_CONSTANCIA_RECEPCION")
	private Long idConstanciaRecepcion;
	
	@Column(name="ID_CONSTANCIA_MIGRACION")
	private Long idConstanciaMigracion;
	
	/**
	 * @return the idConstanciaRecepcion
	 */
	public Long getIdConstanciaRecepcion() {
		return idConstanciaRecepcion;
	}
	/**
	 * @param idConstanciaRecepcion the idConstanciaRecepcion to set
	 */
	public void setIdConstanciaRecepcion(Long idConstanciaRecepcion) {
		this.idConstanciaRecepcion = idConstanciaRecepcion;
	}
	/**
	 * @return the idConstanciaMigracion
	 */
	public Long getIdConstanciaMigracion() {
		return idConstanciaMigracion;
	}
	/**
	 * @param idConstanciaMigracion the idConstanciaMigracion to set
	 */
	public void setIdConstanciaMigracion(Long idConstanciaMigracion) {
		this.idConstanciaMigracion = idConstanciaMigracion;
	}

	
	
}
