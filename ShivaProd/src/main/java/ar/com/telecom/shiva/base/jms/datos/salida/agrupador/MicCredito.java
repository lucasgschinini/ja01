package ar.com.telecom.shiva.base.jms.datos.salida.agrupador;

import java.math.BigInteger;

import ar.com.telecom.shiva.base.dto.SOA;
import ar.com.telecom.shiva.base.enumeradores.TipoCreditoEnum;

@SuppressWarnings("serial")
public class MicCredito extends SOA {
	
	private BigInteger 					codigoCliente;
	private Integer 					cuenta;
	private TipoCreditoEnum 			tipoCredito;
	
	private String						idCobro;

	private MicInformacionNotaCredito 		informacionNotaCredito = new MicInformacionNotaCredito();
	private MicInformacionRemanente 		informacionRemanente = new MicInformacionRemanente();
	private MicInformacionGeneral			informacionGeneral = new MicInformacionGeneral();
	private MicInformacionAdicionalTagetik 	informacionAdicionalTagetikNotaCreditoMic = new MicInformacionAdicionalTagetik();
	private MicInformacionAdicionalDacota 	informacionAdicionalDacota = new MicInformacionAdicionalDacota();
	
	
	public BigInteger getCodigoCliente() {
		return codigoCliente;
	}
	public void setCodigoCliente(BigInteger codigoCliente) {
		this.codigoCliente = codigoCliente;
	}
	public Integer getCuenta() {
		return cuenta;
	}
	public void setCuenta(Integer cuenta) {
		this.cuenta = cuenta;
	}
	public TipoCreditoEnum getTipoCredito() {
		return tipoCredito;
	}
	public void setTipoCredito(TipoCreditoEnum tipoCredito) {
		this.tipoCredito = tipoCredito;
	}
	public MicInformacionNotaCredito getInformacionNotaCredito() {
		return informacionNotaCredito;
	}
	public void setInformacionNotaCredito(
			MicInformacionNotaCredito informacionNotaCredito) {
		this.informacionNotaCredito = informacionNotaCredito;
	}
	public MicInformacionRemanente getInformacionRemanente() {
		return informacionRemanente;
	}
	public void setInformacionRemanente(MicInformacionRemanente informacionRemanente) {
		this.informacionRemanente = informacionRemanente;
	}
	public MicInformacionGeneral getInformacionGeneral() {
		return informacionGeneral;
	}
	public void setInformacionGeneral(MicInformacionGeneral informacionGeneral) {
		this.informacionGeneral = informacionGeneral;
	}
	public MicInformacionAdicionalTagetik getInformacionAdicionalTagetikNotaCreditoMic() {
		return informacionAdicionalTagetikNotaCreditoMic;
	}
	public void setInformacionAdicionalTagetikNotaCreditoMic(
			MicInformacionAdicionalTagetik informacionAdicionalTagetikNotaCreditoMic) {
		this.informacionAdicionalTagetikNotaCreditoMic = informacionAdicionalTagetikNotaCreditoMic;
	}
	public MicInformacionAdicionalDacota getInformacionAdicionalDacota() {
		return informacionAdicionalDacota;
	}
	public void setInformacionAdicionalDacota(
			MicInformacionAdicionalDacota informacionAdicionalDacota) {
		this.informacionAdicionalDacota = informacionAdicionalDacota;
	}
	public String getIdCobro() {
		return idCobro;
	}
	public void setIdCobro(String idCobro) {
		this.idCobro = idCobro;
	}
	
}
