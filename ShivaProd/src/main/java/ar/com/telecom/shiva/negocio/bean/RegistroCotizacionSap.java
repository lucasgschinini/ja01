package ar.com.telecom.shiva.negocio.bean;

public class RegistroCotizacionSap {

	private Long 		numeroRegistro;
	private String 	contenidoRegistro;
	
	/**
	 * @return the numeroRegistro
	 */
	public Long getNumeroRegistro() {
		return numeroRegistro;
	}
	/**
	 * @param numeroRegistro the numeroRegistro to set
	 */
	public void setNumeroRegistro(Long numeroRegistro) {
		this.numeroRegistro = numeroRegistro;
	}
	/**
	 * @return the contenidoRegistro
	 */
	public String getContenidoRegistro() {
		return contenidoRegistro;
	}
	/**
	 * @param contenidoRegistro the contenidoRegistro to set
	 */
	public void setContenidoRegistro(String contenidoRegistro) {
		this.contenidoRegistro = contenidoRegistro;
	}
}
